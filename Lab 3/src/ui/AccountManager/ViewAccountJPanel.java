    private void refreshTextFields() {
        txtrouting.setText(account.getRoutingnumber());
        txtaccount.setText(account.getAccountnumber());
        txtbank.setText(account.getBankname());
    }

    private void setViewMode() {
        
        txtrouting.setEnabled(false);
        txtaccount.setEnabled(false);
        txtbank.setEnabled(false);
        
        btnsave.setEnabled(false);
        btnupdate.setEnabled(true);
    }
    
    private void setEditMode(){
        txtrouting.setEnabled(true);
        txtaccount.setEnabled(true);
        txtbank.setEnabled(true);
        
        btnsave.setEnabled(true);
        btnupdate.setEnabled(false);
    }
    
