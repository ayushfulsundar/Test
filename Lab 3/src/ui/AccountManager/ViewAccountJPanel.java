    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        
        
        String routingNumber = txtrouting.getText();
        String accountNumber = txtaccount.getText();
        String bankName = txtbank.getText();
        
        if(routingNumber.isBlank() || bankName.isBlank() || accountNumber.isBlank())
        {
            JOptionPane.showMessageDialog(null,"All fields are mandatory");
            return;
        }
                

     
        account.setRoutingnumber(routingNumber);
        account.setAccountnumber(accountNumber);
        account.setBankname(bankName);
        
        JOptionPane.showMessageDialog(null, "Account successfully created", "Warning", JOptionPane.WARNING_MESSAGE);
        
        
    }//GEN-LAST:event_btnsaveActionPerformed

